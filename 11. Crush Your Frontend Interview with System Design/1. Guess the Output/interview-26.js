console.log(10+"10");  //"1010"
console.log(10-"10");  //0

// NOTE:- 
// In the first expression, 10 + "10", the number 10 is being concatenated with the 
// string "10", so the result is the string "1010".

// In the second expression, 10 - "10", the string "10" is being type-coerced to a 
// number, so the expression becomes 10 - 10, which evaluates to 0.